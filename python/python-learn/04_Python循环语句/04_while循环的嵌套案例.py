# 补充：输出不换行
print("Hello", end='')  # 不换行输出
print("World", end='')  # 不换行输出
print("")   # 这里相当于换行，下一次打印就是另起一行开始输出了
print('============================================================================')

# 案例-打印九九乘法表
i = 1
j = 1
while i <= 9:
    while j <= i:
        print("%d * %d = %d\t" % (j, i, i*j), end='')
        j += 1
    print("")
    i += 1
    j = 1
